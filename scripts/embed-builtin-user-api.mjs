/**
 * Reads src/core/builtinUserApi/v4.0.js and writes builtinUserApiScript.generated.ts
 * for Metro to bundle as a string constant (RN cannot require raw script as text otherwise).
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const input = path.join(root, 'src/core/builtinUserApi/v4.0.js')
const output = path.join(root, 'src/core/builtinUserApi/builtinUserApiScript.generated.ts')

const script = fs.readFileSync(input, 'utf8')
const header = `/* eslint-disable max-len */
/* auto-generated by scripts/embed-builtin-user-api.mjs — do not edit */
export const BUILTIN_USER_API_SCRIPT: string = ${JSON.stringify(script)}
`

fs.writeFileSync(output, header, 'utf8')
console.log('Wrote', output, `(${(fs.statSync(output).size / 1024).toFixed(1)} KB)`)
